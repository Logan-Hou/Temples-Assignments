function [f,g]=mente(x)
    f = 3*x(1)^2 + x(2)^2 + 3*x(3)^2 + 2*x(4)^2 + 4*x(5) - 8*x(1) - 4*x(2) - 16*x(3) - 5*x(4) - 3*x(5);
    g = [sum(x) - 400;
        x(1)^2 + 2*x(2)^2 + 2*x(3)^2 + x(4)^2 + 6*x(5)^2 - 800;
        2*x(1) + x(2) + 6*x(3) - 200;
        x(3)^2 + x(4)^2 + 5*x(5)^2 - 200];
end

% 初始化随机数生成器，确保每次运行结果相同
rand('state', sum(clock));

% 初始化最优目标函数值和最优解
p0 = 0;
x0 = [];

% 计时开始
tic;

% 循环生成随机解
for i = 1:10^7
    % 生成随机解
    x = 99 * rand(5, 1);
    % 取整和向上取整
    x1 = floor(x);
    x2 = ceil(x);
    
    % 计算当前解的目标函数和约束条件
    [f, g] = mente(x1);
    % 检查是否满足所有约束条件
    if sum(g <= 0) == 4
        % 检查是否是当前最优解
        if p0 <= f
            x0 = x1;
            p0 = f;
        end
    end
    
    % 对向上取整的解也进行同样的检查
    [f, g] = mente(x2);
    if sum(g <= 0) == 4
        if p0 <= f
            x0 = x2;
            p0 = f;
        end
    end
end

% 计时结束
toc;

% 输出最优解和最优目标函数值
x0, p0